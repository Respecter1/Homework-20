/*
	2. Напишіть програму, яка виведе задані аргументи командного рядка у зворотному порядку, відкинувши ім'я програми.
*/
#include <iostream>

int main(int argc, char* argv[]) {
    // Перевіряємо, що є хоча б один аргумент (окрім імені програми)
    if (argc <= 1) {
        std::cerr << "Usage: " << argv[0] << " <arguments>\n";
        return 1;
    }

    // Виводимо аргументи у зворотньому порядку, починаючи з останнього
    for (int i = argc - 1; i > 0; --i) {
        std::cout << argv[i] << " ";
    }
    std::cout << std::endl;

    return 0;
}
